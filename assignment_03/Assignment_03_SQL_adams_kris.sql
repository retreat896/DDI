--  2. Write a function with a cursor that runs a query to select the item name, item
--     price and the number of items in stock for each item that has stock available. The
--     function will read the results from the cursor and will construct a text that will print
--     out the results record by record in separate lines

CREATE OR REPLACE FUNCTION print_in_stock()
RETURNS TEXT
LANGUAGE PLPGSQL
AS $proc$
DECLARE
    item_data RECORD;
    result TEXT DEFAULT '';
    items_cursor CURSOR FOR 
        SELECT item_name, price, item_stock.number_in_stock
        FROM items
        INNER JOIN item_stock ON items.id = item_stock.item_id
        WHERE item_stock.number_in_stock > 0;
BEGIN
    OPEN items_cursor;
    LOOP
        FETCH items_cursor INTO item_data;
        EXIT WHEN NOT FOUND;
        result := result || 'Item Name: ' || item_data.item_name || ', Price: ' || item_data.price || ', Stock: ' || item_data.number_in_stock || E'\n';
    END LOOP;
    CLOSE items_cursor;
    RETURN result;
END
$proc$ ;

SELECT print_in_stock();

--  3. Create a trigger and associated trigger function that will change the value of
--     column “deleted” to TRUE when a row in the items table is deleted. The delete
--     operation will not physically delete the row, rather will change value of “deleted”
--     column to TRUE.

CREATE OR REPLACE FUNCTION mark_delete()
RETURNS TRIGGER
LANGUAGE PLPGSQL
AS $proc$
BEGIN
    UPDATE items SET deleted = TRUE WHERE id = OLD.id;
    RETURN NULL;
END;
$proc$;


-- Create the trigger when a delete occurs on the items table.
CREATE TRIGGER mark_delete_trigger
BEFORE DELETE ON items
FOR EACH ROW
EXECUTE FUNCTION mark_delete();

