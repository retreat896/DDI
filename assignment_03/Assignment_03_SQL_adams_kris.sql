-- 2. Select the email and the number of orders for each user's email address. Consider all non-registered users as one entry. 
--    Order by the number of orders DESC.
SELECT users.email, COUNT(orders.id) 
FROM orders 
    LEFT JOIN users ON orders.user_id = users.id 
GROUP BY users.email 
ORDER BY COUNT(orders.id) DESC;


    -- Hard:

-- 1. Select all order information where the order cost is not equal to the sum of the
--    order contents cost associated with that order. Order by order id ASC.
SELECT * FROM orders WHERE cost != (
    SELECT SUM(items.price * order_contents.quantity) 
    FROM order_contents 
INNER JOIN items ON order_contents.item_id = items.id 
    WHERE order_contents.order_id = orders.id) 
ORDER BY id ASC;

-- 2. Select all registered users id's and emails for users who have purchased an
--    order with more than one quantity of an item. Order by the user id ASC.
SELECT id, email FROM users WHERE id in (
    SELECT user_id FROM orders
    WHERE id IN (
        SELECT order_id FROM order_contents
        WHERE quantity > 1
    )
)
ORDER BY id ASC;



